// function to set up the vertex data and buffers for the skybox
void setupSkyboxBuffers() {
	// generate and bind the VAO and VBO for the skybox
	glGenVertexArrays(1, &skyboxVAO);
	glGenBuffers(1, &skyboxVBO);
	glBindVertexArray(skyboxVAO);
	glBindBuffer(GL_ARRAY_BUFFER, skyboxVBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), skyboxVertices, GL_STATIC_DRAW);

	// set the vertex attribute pointers for the skybox
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

	glBindVertexArray(0);
}

// function to load a cubemap texture from 6 individual texture faces and return the texture ID
unsigned int loadCubemap(std::vector<std::string> faces) {
	unsigned int textureID;

	// generate a texture ID and bind it to the cubemap texture target
	glGenTextures(1, &textureID);
	glBindTexture(GL_TEXTURE_CUBE_MAP, textureID);

	int width, height, nrChannels;

	// turn off vertical flipping of the image on load
	stbi_set_flip_vertically_on_load(false);

	// load each of the 6 faces of the cubemap texture
	for (unsigned int i = 0; i < faces.size(); i++) {
		// load the image data for the current face of the cubemap texture
		unsigned char* data = stbi_load(faces[i].c_str(), &width, &height, &nrChannels, 0);
		// if the image data was successfully loaded, create the texture for the current face of the cubemap texture
		if (data) {
			glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);
			stbi_image_free(data);
		}
		else {
			std::cout << "Cubemap texture failed to load at path: " << faces[i] << std::endl;
			stbi_image_free(data);
		}
	}

	// set the texture parameters for the cubemap texture
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

	// set the texture wrapping parameters for the cubemap texture
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);

	// turn flipping of the image back on for future texture loads
	stbi_set_flip_vertically_on_load(true);

	return textureID;
}